# Page creation helpers for starlightr

#' Update hero image in existing index.mdx
#'
#' @param index_path Path to index.mdx file
#' @param config Configuration list
#' @keywords internal
update_index_hero_image <- function(index_path, config) {
  if (is.null(config$site$logo)) {
    return()  # No logo configured, nothing to update
  }

  content <- readLines(index_path, warn = FALSE)
  content_str <- paste(content, collapse = "\n")

  # Check if hero image already exists
  if (grepl("image:\\s*\\n\\s*file:", content_str)) {
    return()  # Already has hero image
  }

  # Find the hero section and add image after tagline
  # Pattern: find "hero:\n  tagline: ..." and add image after
  hero_image_yaml <- "  image:\n    file: /src/assets/logo.png"

  # Insert image section after tagline line (before actions or other hero content)
  updated <- sub(
    "(hero:\\s*\\n\\s*tagline:[^\\n]*)",
    paste0("\\1\n", hero_image_yaml),
    content_str
  )

  if (updated != content_str) {
    writeLines(updated, index_path)
    cli::cli_alert_success("Updated {.file index.mdx} with hero image")
  }
}

#' Create default index.mdx page
#'
#' @param pkg_path Path to package directory
#' @param output_path Path to output directory
#' @param config Configuration list
#' @keywords internal
create_index_page <- function(pkg_path, output_path, config) {
  index_path <- file.path(output_path, "src", "content", "docs", "index.mdx")

  # If index exists, just update the hero image if needed
  if (file.exists(index_path)) {
    update_index_hero_image(index_path, config)
    return()
  }

  # Get package info
  pkg_name <- get_package_name(pkg_path)
  pkg_desc <- config$site$description %||% "Documentation generated by starlightr"

  # Get hero configuration from YAML
  hero_tagline <- config$home$hero$tagline %||% pkg_desc
  hero_actions <- ""

  if (!is.null(config$home$hero$actions)) {
    actions_yaml <- c()
    for (action in config$home$hero$actions) {
      action_yaml <- sprintf('    - text: %s\n      link: %s\n      icon: %s\n      variant: %s',
                            action$text %||% "Get Started",
                            action$link %||% "/articles/getting-started/",
                            action$icon %||% "right-arrow",
                            action$variant %||% "primary")
      actions_yaml <- c(actions_yaml, action_yaml)
    }
    hero_actions <- paste(actions_yaml, collapse = "\n")
  } else {
    # Default actions
    github_url <- get_github_url(config) %||% "https://github.com/user/repo"
    hero_actions <- sprintf('    - text: Get Started
      link: /articles/getting-started/
      icon: right-arrow
      variant: primary
    - text: View on GitHub
      link: %s
      icon: external
      variant: minimal', github_url)
  }

  # Build hero image section if logo configured
  hero_image <- ""
  if (!is.null(config$site$logo)) {
    hero_image <- "  image:\n    file: /src/assets/logo.png\n"
  }

  # Create default index content with Starlight components
  index_content <- sprintf('---
title: "Welcome to %s"
description: "%s"
template: splash
hero:
  tagline: %s
%s  actions:
%s
---

import { Card, CardGrid } from "@astrojs/starlight/components";

## Quick links

<CardGrid stagger>
  <Card title="Function Reference" icon="list-format">
    Complete API documentation for all functions.
    [Browse Reference >](/reference/)
  </Card>
  <Card title="Articles & Guides" icon="open-book">
    Learn how to use the package with step-by-step guides.
    [Read Articles >](/articles/)
  </Card>
  <Card title="View Source" icon="github">
    Explore the source code and contribute to the project.
    [GitHub Repository >](%s)
  </Card>
  <Card title="Get Help" icon="information">
    Found a bug or need help? Open an issue or discussion.
    [Get Support >](%s/issues)
  </Card>
</CardGrid>
',
    pkg_name,
    pkg_desc,
    hero_tagline,
    hero_image,
    hero_actions,
    get_github_url(config) %||% "https://github.com/user/repo",
    get_github_url(config) %||% "https://github.com/user/repo"
  )

  writeLines(index_content, index_path)
  cli::cli_alert_success("Created {.file index.mdx}")
}

#' Create directory index pages for articles and reference
#'
#' @param pkg_path Path to package directory
#' @param output_path Path to output directory
#' @param config Configuration list
#' @keywords internal
create_directory_indexes <- function(pkg_path, output_path, config) {
  docs_path <- file.path(output_path, "src", "content", "docs")

  # Create articles index only if articles configured
  if (!is.null(config$sidebar$articles)) {
    articles_index_path <- file.path(docs_path, "articles", "index.mdx")
    if (!file.exists(articles_index_path)) {
      pkg_name <- get_package_name(pkg_path)
      articles_content <- sprintf('---
title: "Articles & Guides"
description: "Learn how to use %s with step-by-step guides and tutorials"
---

# Articles & Guides

This section contains guides and tutorials to help you get started with %s and learn about its advanced features.

## Available Articles

Browse the articles in the sidebar to get started!

', pkg_name, pkg_name)

      writeLines(articles_content, articles_index_path)
      cli::cli_alert_success("Created {.file articles/index.mdx}")
    }
  }

  # Create reference index
  reference_index_path <- file.path(docs_path, "reference", "index.mdx")
  if (!file.exists(reference_index_path)) {
    pkg_name <- get_package_name(pkg_path)
    reference_content <- sprintf('---
title: "Function Reference"
description: "Complete API documentation for all %s functions"
---

# Function Reference

This section contains detailed documentation for all functions in the %s package.

## Available Functions

Browse the functions in the sidebar to explore the complete API documentation.

', pkg_name, pkg_name)

    writeLines(reference_content, reference_index_path)
    cli::cli_alert_success("Created {.file reference/index.mdx}")
  }
}
